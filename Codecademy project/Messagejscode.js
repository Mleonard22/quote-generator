function quoteGenerator () {
    let final = Math.floor(Math.random() * quoteArr.length);
    let word = quoteArr[final];
    let lastTime = [];
    for (let i = 0; i < word.length; i++) {
     if (word[i] == '-' || word[i] == '(' || word[i] == ')' ) {
    
        continue}
    lastTime.push(word[i])
    } 
    
    let x = lastTime.join('')
   

    let forQuotes = document.getElementById('forQuotes');
    
    forQuotes.textContent = x;


} 





let theButton = document.querySelector('button')


theButton.addEventListener('click', quoteGenerator)

